"use client";

import type { AnalysisResult } from "@/lib/types/prescription";
import { Check, Copy } from "lucide-react";
import { AnimatePresence, motion } from "motion/react";
import { useTranslations } from "next-intl";
import Image from "next/image";
import { useState } from "react";

type ShareActionsProps = {
  result: AnalysisResult;
};

export function ShareActions({ result }: ShareActionsProps) {
  const t = useTranslations();
  const [copied, setCopied] = useState(false);

  const formatSharingText = () => {
    // Using Unicode escapes for better cross-platform reliability
    const icons = {
      summary: "\uD83D\uDCDD", // 📝
      meds: "\uD83D\uDC8A", // 💊
      clock: "\uD83D\uDD52", // 🕒
      tip: "\uD83D\uDCA1", // 💡
    };

    let text = `*${t("Insights.sharingText")}*\n\n`;

    if (result.summary) {
      text += `${icons.summary} *${t("Insights.summaryLabel")}*:\n${result.summary}\n\n`;
    }

    text += `${icons.meds} *${t("Insights.medications")}*:\n`;
    result.medications.forEach((med) => {
      text += `\n*${med.name}*`;
      if (med.dosage) text += `\n${icons.clock} ${med.dosage}`;
      if (med.usage) text += `\n${icons.tip} ${med.usage}`;
      text += `\n`;
    });

    text += `\n---\n_Generated by Roshetta-AI_`;
    return text;
  };

  const handleCopy = async () => {
    const text = formatSharingText();
    try {
      await navigator.clipboard.writeText(text);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch (err) {
      console.error("Failed to copy text: ", err);
    }
  };

  const handleWhatsApp = () => {
    const text = formatSharingText();
    const url = `https://api.whatsapp.com/send?text=${encodeURIComponent(text)}`;
    window.open(url, "_blank");
  };

  return (
    <div className="flex flex-wrap gap-3">
      <button
        onClick={handleWhatsApp}
        className="flex items-center gap-2 rounded-xl bg-[#25D366] px-4 py-2.5 text-sm font-bold text-white shadow-sm transition-all hover:bg-[#22c35e] active:scale-[0.98]"
      >
        {/* <MessageCircle className="h-4 w-4" /> */}
        <Image
          src="/whatsapp.svg"
          alt="WhatsApp"
          width={16}
          height={16}
          className="text-white invert"
        />
        <span>{t("Insights.shareWhatsApp")}</span>
      </button>

      <button
        onClick={handleCopy}
        className="flex items-center gap-2 rounded-xl bg-white border border-slate-200 px-4 py-2.5 text-sm font-bold text-slate-700 shadow-sm transition-all hover:bg-slate-50 hover:border-slate-300 active:scale-[0.98] relative overflow-hidden"
      >
        <AnimatePresence mode="wait">
          {copied ? (
            <motion.div
              key="check"
              initial={{ opacity: 0, y: 10 }}
              animate={{ opacity: 1, y: 0 }}
              exit={{ opacity: 0, y: -10 }}
              className="flex items-center gap-2"
            >
              <Check className="h-4 w-4 text-brand-teal" />
              <span>{t("Insights.copied")}</span>
            </motion.div>
          ) : (
            <motion.div
              key="copy"
              initial={{ opacity: 0, y: 10 }}
              animate={{ opacity: 1, y: 0 }}
              exit={{ opacity: 0, y: -10 }}
              className="flex items-center gap-2"
            >
              <Copy className="h-4 w-4" />
              <span>{t("Insights.copyResults")}</span>
            </motion.div>
          )}
        </AnimatePresence>
      </button>
    </div>
  );
}
